    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'rate' => 'required',
        ]);

        $rate = Rate::where([
                       'user_id' => Auth::id(),
                       'video_id' => $request->video_id,
                ])->first();

        if ($rate) {
            $rate->rate = $request->rate;
            $rate->save();
            return back();
        } else {
            Rate::create([
                'rate' => $request->rate,
                'user_id' => Auth::id(),
                'video_id' => $request->video_id,
            ]);
            return back();
        }

        
        
    }

